bool is_player_damaged(int chance)
{
    int random;
    int i = 0;
    float numbers[10] = {0.5, 5.77, 1.2, 0.62, 0.11, 1.8, 2.4, 0.95, 0.38, 3.4};
    srand(time(NULL));
    random = rand() % 100;
    i = rand() % 10;
    random *= numbers[i];
    random = random % 100;
    if (random < chance)
    {
        return 1;
    }
    else
    {
        return 0;
    }
}

