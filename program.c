void personage_attack(const int type_of_atack, const int players_class, int *_enemy_hp, int player_power_boost)
{
    int enemy_hp = *_enemy_hp;

    /*
        player_class:
            1 - warrior
            2 - archer
        type_of_atack:
            0 - idle
            1 - simple atack
            2 - power atack
            3 - super atack
    */

    if (players_class == 1)
    {
        if (type_of_atack == 1)
        {
            if (is_player_damaged(WARRIOR_CHANCE_1) == 1)
            {
                enemy_hp -= WARRIOR_POWER_1 * player_power_boost;
            }
        }
        else if (type_of_atack == 2)
        {
            if (is_player_damaged(WARRIOR_CHANCE_2) == 1)
            {
                enemy_hp -= WARRIOR_POWER_2 * player_power_boost;
            }
        }
        else if (type_of_atack == 3)
        {
            if (is_player_damaged(WARRIOR_CHANCE_3) == 1)
            {
                enemy_hp -= WARRIOR_POWER_3 * player_power_boost;
            }
        }
    }
    else if (players_class == 2)
    {
        if (type_of_atack == 1)
        {
            if (is_player_damaged(ARCHER_CHANCE_1) == 1)
            {
                enemy_hp -= ARCHER_POWER_1 * player_power_boost;
            }
        }
        else if (type_of_atack == 2)
        {
            if (is_player_damaged(ARCHER_CHANCE_2) == 1)
            {
                enemy_hp -= ARCHER_POWER_2 * player_power_boost;
            }
        }
        else if (type_of_atack == 3)
        {
            if (is_player_damaged(ARCHER_CHANCE_3) == 1)
            {
                enemy_hp -= ARCHER_POWER_3 * player_power_boost;
            }
        }
    }
    if (enemy_hp < 0)
        enemy_hp = 0;
    *_enemy_hp = enemy_hp;
}

