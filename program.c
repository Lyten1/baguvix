void load_inventory(int inventory[INVENTORY_ROWS][INVENTORY_COLS])
{
    char temp_string[30];
    char name[10];
    name[0] = 0;

    int sp = 0;
    char *count;

    int ch = 0;

    FILE *fin = fopen("inventory.txt", "r");
    if (fin == NULL)
    {
        printf("No load inventory\n");
    }
    else
    {

        for (int i = 0; i < INVENTORY_ROWS; i++)
        {
            int j = 0;
            fgets(temp_string, sizeof(temp_string), fin);
            sp = 0;
            while (temp_string[sp] != ' ')
            {
                strcat(name, (char[2]){temp_string[sp], 0});
                sp++;
            }

            // search first num
            ch = atoi(name);
            inventory[i][j] = ch;
            name[0] = 0;

            j++;

            // search second num
            count = strrchr(temp_string, ' ');
            ch = atoi(count);
            if (ch < 100)
                inventory[i][j] = ch;
            else
                inventory[i][j] = 99;
        }

        fclose(fin);
    }
}

